package Array_Concept;

public class Array_Static {

	public static void main(String[] args) {
		
		//static 	
		//2D & 3D array
			
		 int  a[] 	= new int [3];
		 
		 a[0] = 1;
		 a[1] =2;
		 a[2]=3; 
		
		 
		 for (int i =0;i<3;i++) {
			 
			 System.out.println(a[i]);//a[0] =1;
			 System.out.println(a[i]);//a[1] =2;
			 
			 
		 }
		 
		 int  a1[][] 	= new int [2][2];
		 
		 a1[0][0]= 2;
		 a1[0][1] =3;
		 a1[1][0]= 3;
		 a1[1][1]=0;
		 
		 
		 
		 

	}

}
